def get_sectors_list():
    menu = SECTORS_MENU
    for code in SECTORS_ORDER:
        menu += f"{code} - {SECTORS_DATA[code]['title']}\n"
    menu += "Введите код сферы (например, 2.1) для просмотра деталей\n"
    menu += "Введите 'menu' для возврата в главное меню\n"
    menu += "\n>> "
    return menu

def get_sector_detail(code):
    sector = SECTORS_DATA.get(code)
    if not sector:
        return f"\nВведенная сфера {code} не найдена\n>> "
    detail = f"""

{code} {sector['title']} 

Описание: {sector['description']}

Пример: {sector['example']}

Введите 'back' для возврата к списку сфер
Введите 'menu' для возврата в главное меню

>>> """
    return detail

CLEAR_SCREEN = "\033[2J\033[H"

def handle_client(conn, address):
    """Обработка подключения одного клиента"""
    print(f"Подключен клиент: {address}")
    current_state = "main"
    current_code = None
    conn.send((CLEAR_SCREEN + MAIN_MENU).encode('utf-8'))
    while True:
        try:
            data = conn.recv(4096).decode('utf-8')
            if not data:
                break
            request = data.strip().lower()
            print(f"Получено от {address}: {request}")
            if request == '0' or request == 'bye':
                conn.send("До свидания!".encode('utf-8'))
                break
            if request == 'menu':
                conn.send((CLEAR_SCREEN + MAIN_MENU).encode('utf-8'))
                current_state = "main"
                continue
            if current_state == "main":
                if request == '1':
                    conn.send((CLEAR_SCREEN + LAW_PART1).encode('utf-8'))
                elif request == '2':
                    conn.send((CLEAR_SCREEN + LAW_PART2).encode('utf-8'))
                elif request == '3':
                    conn.send((CLEAR_SCREEN + LAW_PART3).encode('utf-8'))
                elif request == '4':
                    conn.send((CLEAR_SCREEN + get_sectors_list()).encode('utf-8'))
                    current_state = "sectors_list"
                else:
                    conn.send((CLEAR_SCREEN + MAIN_MENU).encode('utf-8'))
            elif current_state == "sectors_list":
                if request in SECTORS_DATA:
                    current_code = request
                    conn.send((CLEAR_SCREEN + get_sector_detail(request)).encode('utf-8'))
                    current_state = "sector_detail"
                else:
                    conn.send((CLEAR_SCREEN + get_sectors_list()).encode('utf-8'))
            elif current_state == "sector_detail":
                if request == 'back':
                    conn.send((CLEAR_SCREEN + get_sectors_list()).encode('utf-8'))
                    current_state = "sectors_list"
                elif request in SECTORS_DATA:
                    current_code = request
                    conn.send((CLEAR_SCREEN + get_sector_detail(request)).encode('utf-8'))
                else:
                    conn.send((CLEAR_SCREEN + get_sector_detail(current_code)).encode('utf-8'))
        except Exception as e:
            print(f"Ошибка при обработке клиента {address}: {e}")
            break
    conn.close()
    print(f"Клиент {address} отключен")

def server():
    HOST = '172.17.8.102'  
    PORT = 6666
    server_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    server_socket.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
    server_socket.bind((HOST, PORT))
    server_socket.listen(5)
    print(f"Сервер запущен на {HOST}:{PORT}")
    print("Ожидание подключений")
    try:
        while True:
            conn, address = server_socket.accept()
            client_thread = threading.Thread(target=handle_client, args=(conn, address))
            client_thread.daemon = True
            client_thread.start()
    except KeyboardInterrupt:
        print("\nСервер остановлен")
    finally:
        server_socket.close()

if __name__ == '__main__':
    server()
